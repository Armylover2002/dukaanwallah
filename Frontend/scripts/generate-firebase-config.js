// ─── FIX #4: Vercel / CI-compatible Firebase config generator ────────────────
// Previously this script only read from a local .env file, which is git-ignored
// and doesn't exist on Vercel/CI builds. Now it reads from process.env first
// (set via Vercel dashboard or CI environment variables) and falls back to
// parsing the local .env file for development convenience.
//
// Output is now a .js file (firebase-web-config.js) instead of .json, so the
// Service Worker can load it synchronously via importScripts() — fixing Bug #1.

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const envPath = path.resolve(__dirname, '../.env');
const outputPath = path.resolve(__dirname, '../public/firebase-web-config.js');

const FIREBASE_KEYS = [
  'VITE_FIREBASE_API_KEY',
  'VITE_FIREBASE_AUTH_DOMAIN',
  'VITE_FIREBASE_PROJECT_ID',
  'VITE_FIREBASE_STORAGE_BUCKET',
  'VITE_FIREBASE_MESSAGING_SENDER_ID',
  'VITE_FIREBASE_APP_ID',
  'VITE_FIREBASE_MEASUREMENT_ID',
  'VITE_FIREBASE_DATABASE_URL',
  'VITE_FIREBASE_VAPID_KEY',
];

try {
  const firebaseConfig = {};

  // Step 1: Read from process.env (works on Vercel, GitHub Actions, etc.)
  for (const key of FIREBASE_KEYS) {
    if (process.env[key]) {
      firebaseConfig[key] = process.env[key];
    }
  }

  // Step 2: Fall back to parsing .env file for local development
  const missingKeys = FIREBASE_KEYS.filter((k) => !firebaseConfig[k]);
  if (missingKeys.length > 0 && fs.existsSync(envPath)) {
    const envFile = fs.readFileSync(envPath, 'utf-8');
    envFile.split('\n').forEach((line) => {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#')) return;
      const splitIdx = trimmed.indexOf('=');
      if (splitIdx === -1) return;
      const key = trimmed.slice(0, splitIdx).trim();
      const value = trimmed.slice(splitIdx + 1).trim();
      if (FIREBASE_KEYS.includes(key) && !firebaseConfig[key]) {
        firebaseConfig[key] = value;
      }
    });
  }

  // Step 3: Validate we have the minimum required keys
  const requiredKeys = [
    'VITE_FIREBASE_API_KEY',
    'VITE_FIREBASE_PROJECT_ID',
    'VITE_FIREBASE_APP_ID',
    'VITE_FIREBASE_MESSAGING_SENDER_ID',
  ];
  const stillMissing = requiredKeys.filter((k) => !firebaseConfig[k]);
  if (stillMissing.length > 0) {
    console.warn(
      '⚠️  firebase-web-config.js: Missing required Firebase keys:',
      stillMissing.join(', '),
      '\n   Background push notifications will not work.',
    );
  }

  // Step 4: Write as a JS file so the Service Worker can importScripts() it synchronously.
  // This attaches the config to `self` (the Service Worker global scope).
  const jsContent = [
    '// Auto-generated by scripts/generate-firebase-config.js — do not edit manually.',
    '// Loaded synchronously by firebase-messaging-sw.js via importScripts().',
    `self.FIREBASE_WEB_CONFIG = ${JSON.stringify(firebaseConfig, null, 2)};`,
    '',
  ].join('\n');

  fs.writeFileSync(outputPath, jsContent, 'utf-8');
  console.log('✅ firebase-web-config.js generated successfully.');
} catch (error) {
  console.error('❌ Error generating firebase-web-config.js:', error);
  process.exit(1);
}
